package core_java;

public class LoopConcept {

    public static void main(String[] args){

        for (int i=1; i<11; i++){
            int five=5;
            int result=five*i;
            System.out.println(result+" : "+i);
        }
        int i=0;
        while (i<10){
            i++;
            int eight=8;
            int output=eight*i;
            System.out.println(output);
        }
    }
}
